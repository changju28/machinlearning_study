A = np.array([
    [3, 3, 2, 3, 1],
    [5, 2, 2, 3, 1],
    [3, 3, np.nan, 3, 1],
    [3, 1, 4, 3, 1],
])

# nan 값 제외 후 덧셈
print(np.nansum(A))
# nan 값 제외 후 평균
print(np.nanmean(A))
